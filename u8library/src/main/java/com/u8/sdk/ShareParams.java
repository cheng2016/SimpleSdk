package com.u8.sdk;


public class ShareParams {
    private String title;
    private String titleUrl;
    private String sourceName;
    private String sourceUrl;
    private String content;
    private String url;
    private String imgUrl;
    private boolean dialogMode;
    private int notifyIcon;
    private String notifyIconText;
    private String comment;

    public String getTitle() {
        return this.title;
    }


    public void setTitle(String title) {
        this.title = title;
    }


    public String getTitleUrl() {
        return this.titleUrl;
    }


    public void setTitleUrl(String titleUrl) {
        this.titleUrl = titleUrl;
    }


    public String getSourceName() {
        return this.sourceName;
    }


    public void setSourceName(String sourceName) {
        this.sourceName = sourceName;
    }


    public String getSourceUrl() {
        return this.sourceUrl;
    }


    public void setSourceUrl(String sourceUrl) {
        this.sourceUrl = sourceUrl;
    }


    public String getContent() {
        return this.content;
    }


    public void setContent(String content) {
        this.content = content;
    }


    public String getImgUrl() {
        return this.imgUrl;
    }


    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }


    public boolean isDialogMode() {
        return this.dialogMode;
    }


    public void setDialogMode(boolean dialogMode) {
        this.dialogMode = dialogMode;
    }


    public int getNotifyIcon() {
        return this.notifyIcon;
    }


    public void setNotifyIcon(int notifyIcon) {
        this.notifyIcon = notifyIcon;
    }


    public String getNotifyIconText() {
        return this.notifyIconText;
    }


    public void setNotifyIconText(String notifyIconText) {
        this.notifyIconText = notifyIconText;
    }


    public String getComment() {
        return this.comment;
    }


    public void setComment(String comment) {
        this.comment = comment;
    }


    public String getUrl() {
        return this.url;
    }


    public void setUrl(String url) {
        this.url = url;
    }
}


